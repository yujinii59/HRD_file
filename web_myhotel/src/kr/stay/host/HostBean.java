package kr.stay.host;

public class HostBean {
	private String hostid, hostpasswd, hosttel;

	public String getHostid() {
		return hostid;
	}

	public void setHostid(String hostid) {
		this.hostid = hostid;
	}

	public String getHostpasswd() {
		return hostpasswd;
	}

	public void setHostpasswd(String hostpasswd) {
		this.hostpasswd = hostpasswd;
	}

	public String getHosttel() {
		return hosttel;
	}

	public void setHosttel(String hosttel) {
		this.hosttel = hosttel;
	}
	
	
}

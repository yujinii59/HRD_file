package kr.stay.hotel;

public class HotelBean {

	private String no, host_id, name, hotel_address, image, detail, min_person, max_person, price;

	public String getNo() {
		return no;
	}

	public void setNo(String no) {
		this.no = no;
	}

	public String getHost_id() {
		return host_id;
	}

	public void setHost_id(String host_id) {
		this.host_id = host_id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getHotel_address() {
		return hotel_address;
	}

	public void setHotel_address(String hotel_address) {
		this.hotel_address = hotel_address;
	}

	public String getImage() {
		return image;
	}

	public void setImage(String image) {
		this.image = image;
	}

	public String getDetail() {
		return detail;
	}

	public void setDetail(String detail) {
		this.detail = detail;
	}

	public String getMin_person() {
		return min_person;
	}

	public void setMin_person(String min_person) {
		this.min_person = min_person;
	}

	public String getMax_person() {
		return max_person;
	}

	public void setMax_person(String max_person) {
		this.max_person = max_person;
	}

	public String getPrice() {
		return price;
	}

	public void setPrice(String price) {
		this.price = price;
	}

	
}
